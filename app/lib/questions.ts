export function getQuestionText(
  userRole: string,
  section: "umum" | "role" | "end",
  questionId: number
): string {
  let questions;
  if (section === "umum") {
    questions = qaUmumQuestions;
  } else if (section === "end") {
    questions = qaEndQuestions;
  } else {
    switch (userRole) {
      case "Mahasiswa/Pelajar Freelancer":
        questions = qaFreelancerQuestions;
        break;
      case "Guru/Dosen/Tenaga Pendidik":
        questions = qaGuruQuestions;
        break;
      case "Mahasiswa/Pelajar Non Freelancer":
        questions = qaStudentQuestions;
        break;
      case "UMKM":
        questions = qaUmkmQuestions;
        break;
      default:
        return `Pertanyaan ${questionId}`;
    }
  }

  const question = questions.find((q) => q.id === questionId);
  return question ? question.text : `Pertanyaan ${questionId}`;
}
